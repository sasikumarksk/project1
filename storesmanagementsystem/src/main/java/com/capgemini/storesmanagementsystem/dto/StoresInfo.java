package com.capgemini.storesmanagementsystem.dto;

public class StoresInfo {
	private int productId;
	private int storesId;
	private int numOfItems;
	private int minStocksPresent;
	public int getProductId() {
		return productId;
	}
	public void setProductId(int productId) {
		this.productId = productId;
	}
	public int getStoresId() {
		return storesId;
	}
	public void setStoresId(int storesId) {
		this.storesId = storesId;
	}
	public int getNumOfItems() {
		return numOfItems;
	}
	public void setNumOfItems(int numOfItems) {
		this.numOfItems = numOfItems;
	}
	public int getMinStocksPresent() {
		return minStocksPresent;
	}
	public void setMinStocksPresent(int minStocksPresent) {
		this.minStocksPresent = minStocksPresent;
	}
		
	

}
